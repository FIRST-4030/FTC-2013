#include "../Sensors/gyro.c"
#define GYRO_OVERRUN (16.0)
#define GYRO_SPEED (50)

void turnToDegree(int target) {
	// Turn left or right
	int speed = GYRO_SPEED;
	if (target < 0) {
		speed *= -1;
	}

	// Offset the target by our expected overrun
	if (target < 0) {
		target += GYRO_OVERRUN;
	} else {
		target -= GYRO_OVERRUN;
	}

	// Turn to the target
	int angle = 0;
	motor[flagMotorA] = speed;
	resetGyro();
	while (abs(angle) < abs(target)) {
		angle = (int)readGyro();
	}
	motor[flagMotorA] = 0;
}

