task main() {

	// Start the gyro and wait for it to settle
	StartTask(gyro);
	while (!GYRO_READY) {
		wait1Msec(5);
	}

	// Turn 90 clockwise
	turnToDegree(90);

	// Turn 45 anti-clockwise
	wait1Msec(500);
	turnToDegree(-45);

	// Turn 45 anti-clockwise
	wait1Msec(500);
	turnToDegree(-45);

	// Turn 180 aclockwise
	wait1Msec(500);
	turnToDegree(180);

	// Turn 90 anti-clockwise
	wait1Msec(500);
	turnToDegree(-90);

	// Turn 90 anti-clockwise
	wait1Msec(500);
	turnToDegree(-90);
}
